#include <libft.h>

int			ft_str_isalpha(const char *str)
{
	while ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
		str++;
	if (*str != '\0')
		return (0);
	return (1);
}
