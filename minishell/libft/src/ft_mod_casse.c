#include "libft.h"

char	ft_upcasse(char c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	return (c);
}

char	ft_downcasse(char c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}
